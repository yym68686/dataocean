const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<LocaleCode>(() => readInitialLocale());
  const intlLocale = locale === "zh" ? "zh-CN" : "en-US";

  useEffect(() => {
    window.localStorage.setItem(LOCALE_STORAGE_KEY, locale);
    document.documentElement.lang = intlLocale;
  }, [intlLocale, locale]);

  const value = useMemo<I18nContextValue>(() => {
    const t = (key: string, vars?: Record<string, string | number>) => interpolate(messages[locale][key as keyof typeof messages.en] ?? key, vars);
    const tx = (text?: string | null) => {
      if (!text) {
        return "";
      }
      return locale === "zh" ? entityZh[text] ?? text : text;
    };
    const te = (group: string, rawValue?: string | null) => {
      if (!rawValue) {
        return "";
      }
      const value = String(rawValue);
      if (locale !== "zh") {
        return value;
      }
      const groupMap = (enumText.zh as Record<string, Record<string, string>>)[group];
      return groupMap?.[value] ?? value;
    };

    return {
      locale,
      intlLocale,
      setLocale: setLocaleState,
      toggleLocale: () => setLocaleState((current) => (current === "zh" ? "en" : "zh")),
      t,
      tx,
      te,
    };
  }, [intlLocale, locale]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used inside I18nProvider");
  }
  return context;
}

export function getBrowserTimeZone() {
  return Intl.DateTimeFormat().resolvedOptions().timeZone;
}

function readInitialLocale(): LocaleCode {
  const stored = window.localStorage.getItem(LOCALE_STORAGE_KEY);
  if (stored === "zh" || stored === "en") {
    return stored;
  }
  return navigator.language.toLowerCase().startsWith("zh") ? "zh" : "en";
}

function interpolate(template: string, vars?: Record<string, string | number>) {
  if (!vars) {
    return template;
  }
  return Object.entries(vars).reduce((value, [key, replacement]) => value.replaceAll(`{${key}}`, String(replacement)), template);
}
